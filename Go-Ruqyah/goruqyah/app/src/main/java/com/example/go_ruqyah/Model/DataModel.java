package com.example.go_ruqyah.Model;

public class DataModel {
    private int id;
    private String rating, nama_peruqyah, alamat;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getNama_peruqyah() {
        return nama_peruqyah;
    }

    public void setNama_peruqyah(String nama_peruqyah) {
        this.nama_peruqyah = nama_peruqyah;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
}
